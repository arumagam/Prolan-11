public class operatorasignment {
	public static void main(String[]args) {
		int x = 10;
		System.out.println(x);
		System.out.println(x +=10);
		System.out.println(x -=10);
		System.out.println(x*=10);
		System.out.println(x/=10);
		System.out.println(x%=10);
		x=10;
		System.out.println((x==1)?11:10);
	}
}