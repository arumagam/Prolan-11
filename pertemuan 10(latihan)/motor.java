public class motor {
	private String nama;
	private int banyak,gear,rantai;
	private int harga;
	
	public String getnama () {
		return nama;
	}
	public void setnama (String nama) {
		this.nama = nama;
	}
	public int getbanyak () {
		return banyak;
	}
	public void setbanyak (int banyak) {
		this.banyak = banyak;
	}
	public int getgear () {
		return gear;
	}
	public void setgear (int gear) {
		this.gear = gear;
	}
	public int getrantai () {
		return rantai;
	}
	public void setrantai (int rantai) {
		this.rantai = rantai;
	}
	public int getharga () {
		return harga;
	}
	public void setharga (int harga) {
		this.harga = harga;
	}
}